<a NAME="q12"><span class="style29"><strong>I am in mainland Europe, can I place an order with you?</strong></span></a>
                <br/>
				<br/>
 <span class="style32">Yes. You're welcome to place order with us. We don't charge you UK VAT if you let us know your EU VAT 
number. We try to achieve the same lead time for orders to most of mainland Europe, but for some addresses please 
allow us up to two days' extra lead time. Apart from this, there is no any other difference to the UK customer's ordering experience. Check 
<a target="_blank" href="/euro/order_from_europe.php">
<span class="style13">here</span></a> to find details of the order process.  </span>
<br><br>

<span class="style29"><strong>Q:&nbsp;</strong></span>
<a NAME="q11"><span class="style29"><strong>What do I do if I need a large volume?</strong></span></a>
<br><span class="style32"><br>Before you buy or procure PCBs from the Far East via an agent, we would like to hear from you. We are certain 
that we can save you money on large volume orders since we don't use any other PCB suppliers for manufacturing.</span> <br><br>

<span class="style29"><strong>Q:&nbsp;</strong></span>
<a NAME="q33"><span class="style29"><strong>Do you send paper invoices?</strong></span></a>
