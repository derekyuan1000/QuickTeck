				<span class="style29"><strong>Is there a minimum quantity?</strong></span></a>
				<br>
<span class="style32"><br>
				
At Quick-teck, there is no minimum quantity! You are free to order just 1 PCB if that is all you require.<br>
</span><br>


<span class="style29"><strong>Q:&nbsp;</strong></span>
<a NAME="q4"><span class="style29"><strong>What's your quality warranty?</strong></span></a> <br>
<span class="style32"><br>We keep our prices low by constantly implementing more efficient manufacturing processes, by developing 
systems that allow us to quickly change setups, by maintaining partnerships with suppliers, and by conservatively managing our business. 
<br><br>
We are committed to providing our customers with PCBs that meet the highest standards for quality and value. 
We comply with the&nbsp; ISO9001:2000 quality management system and UL approvals. <br>
<br>
Low cost doesn't mean that you have to compromise on quality.We guarantee bare boards against quality defects under normal usage, and if there are 
problems, we will:<br>
<br>
&nbsp;1. Replace boards at no charge or refund up to 100% of your payment.<br>
&nbsp;2. Issue a 10% discount (up to &pound;50) voucher on your next order.<br><br>
The above conditions are based on you returning the boards to us. They are also based on a 7 working day limited warranty, valid from our 
delivery date.<br></span>
<br>

